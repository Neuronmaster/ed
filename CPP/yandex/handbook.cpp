#include <iostream>
#include <unordered_map>

std::unordered_map<char, char> char_to_digit = {
    {'b', '1'}, {'f', '1'}, {'p', '1'}, {'v', '1'},
    {'c', '2'}, {'g', '2'}, {'j', '2'}, {'k', '2'}, {'q', '2'}, {'s', '2'}, {'x', '2'}, {'z', '2'},
    {'d', '3'}, {'t', '3'},
    {'l', '4'},
    {'m', '5'}, {'n', '5'},
    {'r', '6'}
};

int main()
{
    std::string word;
    std::cin >> word;
    std::string string_change = word;

    for (size_t i = 0; i < word.length(); ++i)
    {
        char c = word[i];

        // –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –±—É–∫–≤—ã a, e, h, i, o, u, w, y
        if (c == 'a' || c == 'e' || c == 'h' || c == 'i' || c == 'o' || c == 'u' || c == 'w' || c == 'y') {
            continue;
        }

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —Å–∏–º–≤–æ–ª –≤ –∫–∞—Ä—Ç–µ –∏ –¥–æ–±–∞–≤–ª—è–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
        if (char_to_digit.find(c) != char_to_digit.end()) {
            string_change[i] = char_to_digit[c];
        }
    }

    std::cout << string_change << std::endl;
    return 0;
}
